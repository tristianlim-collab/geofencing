const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const dbPath = path.join(root, 'data', 'db.json');
const outputDir = path.join(root, 'supabase');
const outputPath = path.join(outputDir, 'seed.sql');

function sqlString(value) {
  if (value === null || value === undefined) return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlBool(value) {
  if (value === null || value === undefined) return 'NULL';
  return value ? 'true' : 'false';
}

function sqlNumber(value) {
  if (value === null || value === undefined || Number.isNaN(Number(value))) return 'NULL';
  return String(Number(value));
}

function sqlJson(value) {
  if (value === null || value === undefined) return 'NULL';
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

function sqlArrayInt(value) {
  if (!Array.isArray(value)) return 'NULL';
  const clean = value.map(v => Number(v)).filter(v => Number.isInteger(v));
  return `ARRAY[${clean.join(',')}]::integer[]`;
}

const db = JSON.parse(fs.readFileSync(dbPath, 'utf8'));

const lines = [];
lines.push('-- Generated by scripts/supabase-export-sql.js');
lines.push('begin;');
lines.push('');
lines.push('truncate table public.sessions, public.notifications, public.attendance, public.enrollments, public.schedules, public.classes, public.users restart identity cascade;');
lines.push('delete from public.app_settings;');
lines.push('');

const settings = db.settings || {};
lines.push(
  `insert into public.app_settings (id, one_device_per_student, late_grace_minutes, check_out_enabled, face_verification_required, max_face_hamming_distance) values (1, ${sqlBool(settings.oneDevicePerStudent)}, ${sqlNumber(settings.lateGraceMinutes ?? 15)}, ${sqlBool(settings.checkOutEnabled)}, ${sqlBool(settings.faceVerificationRequired)}, ${sqlNumber(settings.maxFaceHammingDistance)});`
);

for (const user of db.users || []) {
  lines.push(
    `insert into public.users (id, role, name, username, student_id, password_hash, course, year_section, device_id, face_descriptor, face_enrolled_at, created_at) values (${sqlString(user.id)}, ${sqlString(user.role)}, ${sqlString(user.name)}, ${sqlString(user.username)}, ${sqlString(user.studentId)}, ${sqlString(user.passwordHash)}, ${sqlString(user.course)}, ${sqlString(user.yearSection)}, ${sqlString(user.deviceId)}, ${sqlString(user.faceDescriptor)}, ${sqlString(user.faceEnrolledAt)}, ${sqlString(user.createdAt)});`
  );
}

for (const cls of db.classes || []) {
  lines.push(
    `insert into public.classes (id, subject_code, subject_name, section, room, location_name, teacher_id, created_at) values (${sqlString(cls.id)}, ${sqlString(cls.subjectCode)}, ${sqlString(cls.subjectName)}, ${sqlString(cls.section)}, ${sqlString(cls.room)}, ${sqlString(cls.locationName)}, ${sqlString(cls.teacherId)}, ${sqlString(cls.createdAt)});`
  );
}

for (const sched of db.schedules || []) {
  lines.push(
    `insert into public.schedules (id, class_id, days_of_week, start_time, end_time, geofence, created_at) values (${sqlString(sched.id)}, ${sqlString(sched.classId)}, ${sqlArrayInt(sched.daysOfWeek)}, ${sqlString(sched.startTime)}, ${sqlString(sched.endTime)}, ${sqlJson(sched.geofence)}, ${sqlString(sched.createdAt)});`
  );
}

for (const enr of db.enrollments || []) {
  lines.push(
    `insert into public.enrollments (id, class_id, student_id, created_at) values (${sqlString(enr.id)}, ${sqlString(enr.classId)}, ${sqlString(enr.studentId)}, ${sqlString(enr.createdAt)});`
  );
}

for (const att of db.attendance || []) {
  lines.push(
    `insert into public.attendance (id, class_id, schedule_id, student_id, date, status, check_in_at, check_out_at, distance_meters, location, device_id, face_verified, face_distance, created_at) values (${sqlString(att.id)}, ${sqlString(att.classId)}, ${sqlString(att.scheduleId)}, ${sqlString(att.studentId)}, ${sqlString(att.date)}, ${sqlString(att.status)}, ${sqlString(att.checkInAt)}, ${sqlString(att.checkOutAt)}, ${sqlNumber(att.distanceMeters)}, ${sqlJson(att.location)}, ${sqlString(att.deviceId)}, ${sqlBool(att.faceVerified)}, ${sqlNumber(att.faceDistance)}, ${sqlString(att.createdAt)});`
  );
}

for (const note of db.notifications || []) {
  lines.push(
    `insert into public.notifications (id, user_id, type, message, read, created_at) values (${sqlString(note.id)}, ${sqlString(note.userId)}, ${sqlString(note.type)}, ${sqlString(note.message)}, ${sqlBool(note.read)}, ${sqlString(note.createdAt)});`
  );
}

for (const sess of db.sessions || []) {
  lines.push(
    `insert into public.sessions (sid, user_id, expires_at) values (${sqlString(sess.sid)}, ${sqlString(sess.userId)}, ${sqlString(sess.expiresAt)});`
  );
}

lines.push('');
lines.push('commit;');
lines.push('');

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputPath, lines.join('\n'));

console.log(`Supabase seed SQL generated: ${outputPath}`);
